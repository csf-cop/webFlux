package com.csf.mama.exception;

/**
 * @author TuanDQ
 *
 */
public class ErrorMessage {

	private String errSubject;

	private String errContent;

	/**
	 * @return the errSubject
	 */
	public String getErrSubject() {
		return errSubject;
	}

	/**
	 * @param errSubject
	 *            the errSubject to set
	 */
	public void setErrSubject(String errSubject) {
		this.errSubject = errSubject;
	}

	/**
	 * @return the errContent
	 */
	public String getErrContent() {
		return errContent;
	}

	/**
	 * @param errContent
	 *            the errContent to set
	 */
	public void setErrContent(String errContent) {
		this.errContent = errContent;
	}

}
